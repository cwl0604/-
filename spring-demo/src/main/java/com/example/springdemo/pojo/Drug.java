package com.example.springdemo.pojo;

public class Drug {
    private int drugid;
    private String drugname;
    private String drugstandard;
    private String drugstate;
    private String drugunit;
    private int drugcount;
    private int drugtotal;
    private String drugtype;
    public int getDrugid() {
        return drugid;
    }
    public void setDrugid(int drugid) {
        this.drugid = drugid;
    }
    public String getDrugname() {
        return drugname;
    }
    public void setDrugname(String drugname) {
        this.drugname = drugname;
    }
    public String getDrugstandard() {
        return drugstandard;
    }
    public void setDrugstandard(String drugstandard) {
        this.drugstandard = drugstandard;
    }
    public String getDrugstate() {
        return drugstate;
    }
    public void setDrugstate(String drugstate) {
        this.drugstate = drugstate;
    }
    public String getDrugunit() {
        return drugunit;
    }
    public void setDrugunit(String drugunit) {
        this.drugunit = drugunit;
    }
    public int getDrugcount() {
        return drugcount;
    }
    public void setDrugcount(int drugcount) {
        this.drugcount = drugcount;
    }
    public int getDrugtotal() {
        return drugtotal;
    }
    public void setDrugtotal(int drugtotal) {
        this.drugtotal = drugtotal;
    }
    public String getDrugtype() {
        return drugtype;
    }
    public void setDrugtype(String drugtype) {
        this.drugtype = drugtype;
    }
}
